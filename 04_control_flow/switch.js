const month = "march"

switch (month) {
    case "jan":
        console.log("January");
        break;
    case "feb":
        console.log("feb");
        break;
    case "march":
        console.log("march");
        break;
    case "april":
        console.log("april");
        break;
    case "may":
        console.log("april");
        break;

    default:
        console.log("default case match");
        break;
}
//* break is very important , else full code after matching will execute other than default  
